package com.ikramdg.core;

import java.util.List;

public class ProgramArguments {

	private String operationArgument;
	private List<String> infoArguments;

	public String getOperationArgument() {
		return operationArgument;
	}

	public void setOperationArgument(String operationArgument) {
		this.operationArgument = operationArgument;
	}

	public List<String> getInfoArguments() {
		return infoArguments;
	}

	public void setInfoArguments(List<String> infoArguments) {
		this.infoArguments = infoArguments;
	}

}
